export function normalizeLocale(locale) {
  const baseLocale = locale?.toLowerCase().split('-')[0];
  return SUPPORTED_LOCALES.includes(baseLocale) ? baseLocale : null;
}

export function detectLocale(storedLocale, browserLanguages = []) {
  const saved = normalizeLocale(storedLocale);
  if (saved) return saved;

  for (const language of browserLanguages) {
    const browserLocale = normalizeLocale(language);
    if (browserLocale) return browserLocale;
  }

  return 'en';
}

export function translate(locale, key, variables = {}) {
  const selected = translations[normalizeLocale(locale) ?? 'en'];
  const template = selected[key] ?? translations.en[key] ?? key;

  return Object.entries(variables).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template,
  );
}
